class Solution:
    def canPartition(self, nums: List[int]) -> bool:
        n = len(nums)
        if n < 2:
            return False

        total = sum(nums)
        if total % 2 == 1:
            return False

        target = total // 2
        if max(nums) > target:
            return False

        dp = [False] * (target + 1) 
        dp[0] = True
        dp[nums[0]] = True  # 第一个数
        for i in range(1, n):
            num = nums[i]
            for j in range(target, 0, -1):  # 方向
                if j >= num:
                    dp[j] = dp[j] or dp[j - num]
        return dp[target]
